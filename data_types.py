#integer
num = 10
print(type(num))

#float
pi = 3.15
print(type(pi))

#string
greeting = "hello bhai"
print(type(greeting))

#list
numbers = [1,2,3,4,5]
print(type(numbers))

#tuple
tulpa = (1,2,3,4)
print(type(tulpa))

#dictionary
handbook = {"uj": 21, "ankro": 20}
print(type(handbook))

#set
unique_nos = {1,2,3,4}
print(type(unique_nos))

#boolean
is_valid = True
print(type(is_valid))